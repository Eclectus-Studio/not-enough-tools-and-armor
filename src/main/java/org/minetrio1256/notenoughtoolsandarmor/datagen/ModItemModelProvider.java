    private ItemModelBuilder modded_tool_itemItem(RegistryObject<Item> item) {
        // Retrieve the number of sticks from the HashMap
        int sticks = StickCustomModelData.stickCustomModelData.size();

        // Create the base item model
        ItemModelBuilder builder = withExistingParent(item.getId().getPath(),
                ResourceLocation.parse("item/handheld"))
                .texture("layer0", ResourceLocation.parse(Main.MOD_ID + ":item/" + item.getId().getPath()));

        // Add overrides for each custom model data value
        for (int i = 1; i <= sticks; i++) {
            // Get the stick RegistryObject
            RegistryObject<Item> stickItem = StickCustomModelData.stickCustomModelData.get(i);
            if (stickItem == null) continue; // Skip if stickItem is null

            // Generate and register the custom model file
            CustomModelDataItem(item, stickItem);

            // Get the stick name
            String stickName = stickItem.getId().getPath();

            // Create the model path for the override
            String modelPath = Main.MOD_ID + ":item/" + item.getId().getPath() + "_with_" + stickName;

            // Add the custom model data override
            builder.override()
                    .predicate(ResourceLocation.parse("custom_model_data"), i)
                    .model(new ModelFile.UncheckedModelFile(ResourceLocation.parse(modelPath)))
                    .end();
        }
        System.gc();
        LOGGER.debug(item.getId().getPath());

        return builder; // Ensure this builder is returned and registered by the data generator
    }

    public ItemModelBuilder CustomModelDataItem(RegistryObject<Item> item, RegistryObject<Item> stick) {
        // Generate a model file for a specific stick and tool combination
        return withExistingParent(item.getId().getPath() + "_with_" + stick.getId().getPath(),
                ResourceLocation.parse("item/handheld"))
                .texture("layer0", ResourceLocation.parse(Main.MOD_ID + ":item/" + stick.getId().getPath()))
                .texture("layer1", ResourceLocation.parse(Main.MOD_ID + ":item/" + item.getId().getPath()));
    }

